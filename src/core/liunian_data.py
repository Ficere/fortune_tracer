# 运势等级划分
LEVEL_THRESHOLDS = [
    (85, "大吉"),
    (70, "吉"),
    (55, "平"),
    (40, "凶"),
    (0, "大凶"),
]


def get_level(score: int) -> str:
    """根据分数获取运势等级"""
    for threshold, level in LEVEL_THRESHOLDS:
        if score >= threshold:
            return level
    return "大凶"
